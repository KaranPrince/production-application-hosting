#!/usr/bin/env bash

set -Eeuo pipefail

#########################################
# Repository
#########################################

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../.." && pwd)"

#########################################
# Project
#########################################

PROJECT_NAME="Production Application Hosting"

VERSION="$(cat "${REPO_ROOT}/VERSION" 2>/dev/null || echo "development")"

#########################################
# Docker
#########################################

COMPOSE_FILE="${REPO_ROOT}/infrastructure/edge/docker/docker-compose.yaml"

#########################################
# PKI
#########################################

PKI_DIR="${REPO_ROOT}/infrastructure/pki"

ROOT_CA_DIR="${PKI_DIR}/root-ca"

SERVER_DIR="${PKI_DIR}/server"

GENERATED_DIR="${PKI_DIR}/generated"

#########################################
# Colors
#########################################

RED="\033[0;31m"
GREEN="\033[0;32m"
YELLOW="\033[1;33m"
BLUE="\033[0;34m"
NC="\033[0m"

#########################################
# Logging
#########################################

info() {
    echo -e "${BLUE}[INFO]${NC} $*"
}

success() {
    echo -e "${GREEN}[SUCCESS]${NC} $*"
}

warn() {
    echo -e "${YELLOW}[WARN]${NC} $*"
}

error() {
    echo -e "${RED}[ERROR]${NC} $*"
}

die() {
    error "$*"
    exit 1
}

################################################################################
# Header
################################################################################

header() {

cat <<EOF

==============================================================
$PROJECT_NAME
Version : $VERSION
==============================================================

EOF

}

################################################################################
# Section
################################################################################

section() {

echo
echo "--------------------------------------------------------------"
echo "$1"
echo "--------------------------------------------------------------"

}

################################################################################
# Check command
################################################################################

check_command() {

local cmd="$1"

if command -v "$cmd" >/dev/null 2>&1
then
    printf " %-22s ✓\n" "$cmd"
else
    die "$cmd not installed."
fi

}

################################################################################
# Check file
################################################################################

check_file() {

local file="$1"

[ -f "$file" ] || die "Missing file: $file"

}

################################################################################
# Check directory
################################################################################

check_directory() {

local dir="$1"

[ -d "$dir" ] || die "Missing directory: $dir"

}